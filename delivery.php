document.addEventListener('DOMContentLoaded', function() {
  // Находим все элементы с вопросами
  const questionBlocks = document.querySelectorAll('.question_block_p');
  
  // Добавляем обработчик клика для каждого блока
  questionBlocks.forEach(block => {
    // Находим стрелку внутри блока
    const arrow = block.querySelector('.j img');
    // Находим блок с текстом ответа
    const answer = block.querySelector('.d');
    // Находим все элементы с классом question_content-p2 (текст ответа)
    const answerTexts = block.querySelectorAll('.question_content-p2');
    
    // Скрываем все ответы изначально (кроме первого, если нужно)
    answerTexts.forEach(text => {
      text.style.display = 'none';
    });
    
    // Добавляем обработчик клика
    block.addEventListener('click', function() {
      // Переключаем класс active для анимации стрелки
      this.classList.toggle('active');
      
      // Переключаем отображение текста ответа
      answerTexts.forEach(text => {
        if (text.style.display === 'none') {
          text.style.display = 'block';
        } else {
          text.style.display = 'none';
        }
      });
      
      // Анимация стрелки
      if (this.classList.contains('active')) {
        arrow.style.transform = 'rotate(180deg)';
      } else {
        arrow.style.transform = 'rotate(0deg)';
      }
    });
  });
});
</script>
